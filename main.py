while running:
    # poll for events
    # pygame.QUIT event means the user clicked X to close your window
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            running = False
        pressed=pygame.mouse.get_pressed()[0]
        
    mouse_loc = pygame.mouse.get_pos()
    moved =( pygame.mouse.get_rel()!=(0,0) )

    #update the drawing
    if pressed and moved:
        saved.draw(canva_size,screen,number,draw_size,mouse_loc,radius=2)
    if reset.pressed(mouse_loc,pressed)==True:
        number = np.zeros((draw_size[0],draw_size[1]))
    if predict.pressed(mouse_loc,pressed)==True:
        print(repr(number))
        model.eval()
        numbercopy=np.copy(number)
        x = torch.FloatTensor(np.array([numbercopy.T]))
        with torch.no_grad():
            x = x.to(device)
            pred = model(x)
            predicted = classes[pred[0].argmax(0)]
        print("The prediction is "+ predicted )
        time.sleep(1)

    # fill the screen with a color to wipe away anything from last frame
    screen.fill("black")
    dp.aff(canva_size,screen,number,draw_size)
    reset.display(screen)
    predict.display(screen)
    pygame.draw.circle(screen, (255,255,255), mouse_loc, circle_radius)
    

    # flip() the display to put your work on screen
    pygame.display.flip()

    clock.tick(60)  # limits FPS to 60

pygame.quit()